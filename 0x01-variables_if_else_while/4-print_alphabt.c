#include <stdio.h>
/**
 * main - function
 * Description: print alphabet in small letters except e and q
 * Return: 0
 */
int main(void)
{
        char ch;

        for (ch = 'a'; ch <= 'z'; ch++)
		if (ch = 'e' && ch = 'q')
			return;
		else
                	putchar(ch);
        putchar('\n');
        return (0);
}

